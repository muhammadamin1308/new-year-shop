let cardId

function attachCardClickListeners(gifts) {
  document.querySelectorAll(".column-3").forEach((card) => {
    card.addEventListener("click", () => {
      cardId = parseInt(card.getAttribute("id"), 10);
      displayModal(gifts);
      document.body.classList.toggle('no-scroll');
      const modalCont = document.getElementById("modal-cont");
      if (modalCont) {
        modalCont.addEventListener("click", (e) => {
          if (e.target === modalCont) {
            modalCont.style.display = "none";
            document.body.classList.remove('no-scroll');
          }
        });
      }
    });
  });
}

